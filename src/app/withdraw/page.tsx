export default function Withdraw() {
  const [status, setStatus] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);

  async function withdraw() {
    try {
      if (!window.ethereum) {
        setStatus("MetaMask not detected!");
        return;
      }

      setLoading(true);
      setStatus(null);

      // Connect to MetaMask
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const contract = new ethers.Contract(
        CONTRACT_ADDRESS,
        CONTRACT_ABI,
        signer
      );

      // Call withdrawFunds function
      const tx = await contract.withdrawFunds();
      setStatus("Transaction sent! Waiting for confirmation...");

      await tx.wait(); // Wait for the transaction to be confirmed

      setStatus("Withdrawal successful!");
    } catch (error) {
      console.error(error);
      setStatus("Withdrawal failed. Check console for details.");
    } finally {
      setLoading(false);
    }
  }

  return (
    <div className="flex flex-col items-center p-6 bg-gray-800 text-white rounded-lg">
      <h2 className="text-2xl font-bold mb-4">Withdraw Funds</h2>
      <button
        onClick={withdraw}
        disabled={loading}
        className="bg-blue-500 hover:bg-blue-600 px-6 py-2 rounded-lg disabled:bg-gray-500"
      >
        {loading ? "Processing..." : "Withdraw"}
      </button>
      {status && <p className="mt-4">{status}</p>}
    </div>
  );
}
